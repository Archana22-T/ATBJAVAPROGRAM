package sept.ex_16092024_Operators;

public class Lab031_Arithmetic_operator {
    public static void main(String[] args) {
        // Arithmetic operator
        // +,-,/,* // binary operator
        int a = 10;
        int b = 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        // % - Modulus- Remainders
        System.out.println(a%b);

        System.out.println(67%13);

    }
}
