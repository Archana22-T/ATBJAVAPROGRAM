package sept.ex_16092024_Operators;

public class Lab037_Compound_Assignment_Operators {
    public static void main(String[] args) {
        // Compound assignment operators
        int a = 10;
        // +=, -=, *=, /=
        a += 20; // a = a + 20
        a -= 20; // a = a - 20
        a /= 10; // a = a / 10


        System.out.println(a);

    }
}
